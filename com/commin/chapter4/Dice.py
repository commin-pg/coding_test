"""
1에서부터 6까지의 눈을 가진 3개의 주사위를 던져서 다음과 같은 규칙에 따라 상금을 받는 게임이 있다.

같은 눈이 3개가 나오면 10,000원+(같은 눈)×1,000원의 상금을 받게 된다.
같은 눈이 2개만 나오는 경우에는 1,000원+(같은 눈)×100원의 상금을 받게 된다.
모두 다른 눈이 나오는 경우에는 (그 중 가장 큰 눈)×100원의 상금을 받게 된다.
예를 들어, 3개의 눈 3, 3, 6이 주어지면 상금은 1,000+3×100으로 계산되어 1,300원을 받게 된다. 또 3개의 눈이 2, 2, 2로 주어지면 10,000+2×1,000 으로 계산되어 12,000원을 받게 된다. 3개의 눈이 6, 2, 5로 주어지면 그중 가장 큰 값이 6이므로 6×100으로 계산되어 600원을 상금으로 받게 된다.

3개 주사위의 나온 눈이 주어질 때, 상금을 계산하는 프로그램을 작성 하시오.

입력
첫째 줄에 3개의 눈이 빈칸을 사이에 두고 각각 주어진다.

출력
첫째 줄에 게임의 상금을 출력 한다.
"""
# 1. 같은 눈이 3개가 나오면 10000원 + 같은눈 숫자 * 1000 원의 상금
# 2. 같은 눈이 2개만 나오는 경우 1000원 + 같은눈 숫자 * 100원의 상금

dice_num_list = list(map(int, input().split()))
dice_num_list_set = set(dice_num_list)
if len(dice_num_list_set) >= 3:
    # 3. 모두 다른 눈이 나오는 경우에는 가장 큰 눈 * 100원의 상금
    print(max(dice_num_list) * 100)
else:
    for i in dice_num_list:
        cnt = dice_num_list.count(i)
        if cnt == 1:
            continue
        elif cnt == 2:
            print(1000 + i * 100)
            break
        else:
            print(10000 + i * 1000)
            break

# 남 답
lst = sorted(list(map(int, input().split())))

if len(set(lst)) == 1:
    print(10000 + lst[0] * 1000)
elif len(set(lst) == 2):
    print(1000 + lst[1] * 1000)
else:
    print(max(lst) * 100)
